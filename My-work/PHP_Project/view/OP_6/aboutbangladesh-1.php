                                <h2><strong>Historical background</strong></h2>
								<h5>
                                    Main article: Partition of Bengal (1905)
                                    The song was written in 1905 during the first partition of Bengal, when the ruling British Empire had an undivided India's province of Bengal split into two parts; the decision was announced on 19 July by the then-Viceroy of India Lord Curzon, taking effect on 16 October. This divide of Bengal, being along communal linesEast Bengal having a majority of Muslims and West Bengal having a majority of Hindusis claimed to have undermined India's national movement against British imperialism and to have been politically motivated. Along with a host of others, songs such as this were meant to rekindle the unified spirit of Bengal, to raise public consciousness against the communal political divide.

                                    The lyrics first appeared in the September issues of "Bongodorshon" and "Baul" simultaneously, in 1905. The song along with the musical notation (referred to as swaralipi in Bengali), first appeared in the periodical musical journal Shongeet Biggnan Probeshika in the same month and year. Indira Devi, Tagore's niece, Satyendranath Tagore's daughter, jotted down the musical notation hearing it from Tagore himself (this was the common norm, Tagore singing the song, and someone formally jotting down the musical notations).
								</h5>
